export const TypeDoc = ({
    name,
    children,
}: {
    name: string;
    children?: React.ReactNode;
}) => {
    const [page, field] = name.split(".");
    return (
        <a
            target="_blank"
            rel="noopener noreferrer"
            href={`https://renproject.github.io/ren-js-v3-docs/${lookup[page]}${
                field ? `#${field}` : ""
            }`}
        >
            {children || name}
            {!lookup[page] ? (
                <span style={{ color: "#bf616a" }}> (Link not found)</span>
            ) : null}
        </a>
    );
};
